package com.project.shoponline.model.module4;

public class AdvertiserPaymentHistory {
	private String AdvertiserID;
	private String Advertiser ;
	private String InvoiceNumber;
	private String TransactionCommissions;
	private String BonusAmount;
	private String CPMAandCPCCommission;
	private String CancelledCommissions;
	private String PreviouslyHeldCommissions;
	private String PaymentAmount;
	private String AdvertiserPaymentDate;
	public String getAdvertiserID() {
		return AdvertiserID;
	}
	public void setAdvertiserID(String advertiserID) {
		AdvertiserID = advertiserID;
	}
	public String getAdvertiser() {
		return Advertiser;
	}
	public void setAdvertiser(String advertiser) {
		Advertiser = advertiser;
	}
	public String getInvoiceNumber() {
		return InvoiceNumber;
	}
	public void setInvoiceNumber(String invoiceNumber) {
		InvoiceNumber = invoiceNumber;
	}
	public String getTransactionCommissions() {
		return TransactionCommissions;
	}
	public void setTransactionCommissions(String transactionCommissions) {
		TransactionCommissions = transactionCommissions;
	}
	public String getBonusAmount() {
		return BonusAmount;
	}
	public void setBonusAmount(String bonusAmount) {
		BonusAmount = bonusAmount;
	}
	public String getCPMAandCPCCommission() {
		return CPMAandCPCCommission;
	}
	public void setCPMAandCPCCommission(String cPMAandCPCCommission) {
		CPMAandCPCCommission = cPMAandCPCCommission;
	}
	public String getCancelledCommissions() {
		return CancelledCommissions;
	}
	public void setCancelledCommissions(String cancelledCommissions) {
		CancelledCommissions = cancelledCommissions;
	}
	public String getPreviouslyHeldCommissions() {
		return PreviouslyHeldCommissions;
	}
	public void setPreviouslyHeldCommissions(String previouslyHeldCommissions) {
		PreviouslyHeldCommissions = previouslyHeldCommissions;
	}
	public String getPaymentAmount() {
		return PaymentAmount;
	}
	public void setPaymentAmount(String paymentAmount) {
		PaymentAmount = paymentAmount;
	}
	public String getAdvertiserPaymentDate() {
		return AdvertiserPaymentDate;
	}
	public void setAdvertiserPaymentDate(String advertiserPaymentDate) {
		AdvertiserPaymentDate = advertiserPaymentDate;
	}

}
