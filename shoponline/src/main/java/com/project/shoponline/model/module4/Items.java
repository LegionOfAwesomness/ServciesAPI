package com.project.shoponline.model.module4;

public class Items {
	private int quantity;
	private String perItemSaleAmountPubCurrency;
	private String totalCommissionPubCurrency;
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	public String getPerItemSaleAmountPubCurrency() {
		return perItemSaleAmountPubCurrency;
	}
	public void setPerItemSaleAmountPubCurrency(String perItemSaleAmountPubCurrency) {
		this.perItemSaleAmountPubCurrency = perItemSaleAmountPubCurrency;
	}
	public String getTotalCommissionPubCurrency() {
		return totalCommissionPubCurrency;
	}
	public void setTotalCommissionPubCurrency(String totalCommissionPubCurrency) {
		this.totalCommissionPubCurrency = totalCommissionPubCurrency;
	}
	
}
